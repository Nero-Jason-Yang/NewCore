#import "UIColor+Skin.h"

@implementation UIColor (Skin)

+ (UIColor *)skinWhiteColor
{
    return [UIColor whiteColor];
}

+ (UIColor *)skinGreenColor
{
    return [UIColor colorWithRed:32.0/255 green:151.0/255 blue:71.0/255 alpha:1];
}

+ (UIColor *)skinLightGreenColor
{
    return [UIColor colorWithRed:48.0/255 green:226.0/255 blue:106.0/255 alpha:1];
}

+ (UIColor *)skinDeepGreenColor
{
    return [UIColor colorWithRed:15.0/255 green:71.0/255 blue:34.0/255 alpha:1];
}

@end
